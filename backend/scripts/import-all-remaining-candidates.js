async function importRemainingCandidates() {
  try {
    console.log('ЁЯФД Connecting to database...\n');
    
    // Get all parties first
    const [parties] = await db.query('SELECT id, short_code FROM Parties');
    const partyMap = {};
    parties.forEach(p => {
      partyMap[p.short_code] = p.id;
    });
    
    console.log('ЁЯУЛ Available parties:', Object.keys(partyMap).join(', '), '\n');
    
    // Get all constituencies
    const [constituencies] = await db.query('SELECT id, name_hindi FROM Constituencies');
    const constituencyMap = {};
    constituencies.forEach(c => {
      constituencyMap[c.name_hindi] = c.id;
    });
    
    console.log(`ЁЯУН Loaded ${constituencies.length} constituencies\n`);
    
    // Track statistics
    let totalImported = 0;
    let totalSkipped = 0;
    const skippedDetails = [];
    
    // Import each party
    for (const [partyCode, candidates] of Object.entries(allRemainingCandidates)) {
      console.log(`\nЁЯУж Processing ${partyCode} (${candidates.length} candidates)...`);
      
      const partyId = partyMap[partyCode];
      if (!partyId) {
        console.log(`тЭМ Party ${partyCode} not found in database! Skipping...`);
        totalSkipped += candidates.length;
        continue;
      }
      
      let imported = 0;
      let skipped = 0;
      
      for (const [constituencyName, candidateName] of candidates) {
        // Try to find constituency (with mapping)
        const mappedName = nameMapping[constituencyName] || constituencyName;
        const constituencyId = constituencyMap[mappedName] || constituencyMap[constituencyName];
        
        if (!constituencyId) {
          skipped++;
          skippedDetails.push({
            party: partyCode,
            constituency: constituencyName,
            candidate: candidateName,
            reason: 'Constituency not found'
          });
          continue;
        }
        
        // Insert candidate
        try {
          await db.query(
            'INSERT INTO Candidates (name_hindi, constituency_id, party_id) VALUES (?, ?, ?)',
            [candidateName, constituencyId, partyId]
          );
          imported++;
        } catch (err) {
          skipped++;
          skippedDetails.push({
            party: partyCode,
            constituency: constituencyName,
            candidate: candidateName,
            reason: err.message
          });
        }
      }
      
      console.log(`  тЬЕ Imported: ${imported}`);
      console.log(`  тЪая╕П  Skipped: ${skipped}`);
      totalImported += imported;
      totalSkipped += skipped;
    }
    
    console.log('\n' + '='.repeat(60));
    console.log(`\nЁЯОЙ IMPORT COMPLETE!`);
    console.log(`   тЬЕ Total Imported: ${totalImported}`);
    console.log(`   тЪая╕П  Total Skipped: ${totalSkipped}`);
    
    if (skippedDetails.length > 0) {
      console.log(`\nтЪая╕П  Skipped Details:`);
      skippedDetails.forEach((s, i) => {
        console.log(`   ${i + 1}. [${s.party}] ${s.constituency} - ${s.candidate}: ${s.reason}`);
      });
    }
    
    // Show final counts
    console.log('\nЁЯУК Final candidate counts by party:');
    for (const partyCode of Object.keys(allRemainingCandidates)) {
      const partyId = partyMap[partyCode];
      if (partyId) {
        const [result] = await db.query(
          'SELECT COUNT(*) as count FROM Candidates WHERE party_id = ?',
          [partyId]
        );
        console.log(`   ${partyCode}: ${result[0].count} candidates`);
      }
    }
    
    // Show total
    const [totalResult] = await db.query('SELECT COUNT(*) as count FROM Candidates');
    console.log(`\nЁЯОп TOTAL CANDIDATES IN DATABASE: ${totalResult[0].count}`);
    
  } catch (error) {
    console.error('тЭМ Error:', error.message);
    throw error;
  } finally {
    await db.end();
  }
}

