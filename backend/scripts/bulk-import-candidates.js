async function importCandidates() {
  console.log('\nüì• Importing candidates from CSV data...');
  
  let totalImported = 0;
  let totalSkipped = 0;
  
  for (const [partyCode, candidates] of Object.entries(csvData)) {
    console.log(`\nüìã Processing ${partyCode} (${candidates.length} candidates)...`);
    
    const partyId = await getPartyId(partyCode);
    if (!partyId) {
      console.error(`‚úó Skipping ${partyCode} - party not found in database`);
      totalSkipped += candidates.length;
      continue;
    }
    
    let imported = 0;
    let skipped = 0;
    
    for (const [constituencyName, candidateName] of candidates) {
      const constituencyId = await getConstituencyId(constituencyName);
      
      if (!constituencyId) {
        console.warn(`  ‚ö† Skipping: ${candidateName} (${constituencyName})`);
        skipped++;
        continue;
      }
      
      try {
        await db.query(
          `INSERT INTO Candidates (name_hindi, party_id, constituency_id, created_at) 
           VALUES (?, ?, ?, NOW())`,
          [candidateName, partyId, constituencyId]
        );
        imported++;
        
        if (imported % 10 === 0) {
          process.stdout.write(`  ‚úì ${imported}/${candidates.length}...\r`);
        }
      } catch (error) {
        console.error(`  ‚úó Failed to insert ${candidateName}:`, error.message);
        skipped++;
      }
    }
    
    console.log(`  ‚úì Imported: ${imported}, Skipped: ${skipped}`);
    totalImported += imported;
    totalSkipped += skipped;
  }
  
  console.log(`\n‚úÖ Import complete!`);
  console.log(`  Total imported: ${totalImported}`);
  console.log(`  Total skipped: ${totalSkipped}`);
  
  return true;
}

